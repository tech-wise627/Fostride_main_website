values ('bin_001', 'Main Campus', 'normal')
on conflict (bin_id) do nothing;


-- 4. CREATE WASTE LOGS TABLE (For Dashboard Data)
create table if not exists public.r3bin_waste_logs (
    id bigint generated by default as identity primary key,
    bin_id text not null references public.r3bin_registry(bin_id),
    waste_type text not null, -- 'plastic', 'metal', 'paper', 'mixed'
    weight float, -- Optional if just counting items
    updated_at timestamptz default now()
);

-- Enable RLS
alter table public.r3bin_waste_logs enable row level security;
create policy "Authenticated read logs" on r3bin_waste_logs for select to authenticated using (true);
create policy "Service role write logs" on r3bin_waste_logs for insert to service_role with check (true);


-- 5. CREATE CONTACT MESSAGES
create table if not exists public.contact_messages (
    id bigint generated by default as identity primary key,
    name text,
    email text,
    phone text,
    message text,
    created_at timestamptz default now()
);

alter table public.contact_messages enable row level security;
create policy "Public insert contact" on contact_messages for insert to public with check (true);


-- 6. ENSURE USER BINS EXISTS (For User-Bin Linking)
create table if not exists public.user_bins (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users not null,
  bin_id text not null,
  created_at timestamptz default now(),
  unique (user_id, bin_id)
);

alter table public.user_bins enable row level security;
create policy "Users view own bins" on user_bins for select using (auth.uid() = user_id);
create policy "Users insert own bins" on user_bins for insert with check (auth.uid() = user_id);

