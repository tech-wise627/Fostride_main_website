-- 2. CREATE REGISTRY TABLE (For Bin ID Verification)
create table if not exists public.r3bin_registry (
    id bigint generated by default as identity primary key,
    bin_id text not null unique,
    location text,
    status text default 'normal',
    created_at timestamptz default now()
);

-- Enable RLS
alter table public.r3bin_registry enable row level security;
create policy "Public read registry" on r3bin_registry for select using (true);
create policy "Authenticated read registry" on r3bin_registry for select to authenticated using (true);


-- 3. INSERT DEFAULT BIN (Crucial for Signup Verification)
